// Axis-aligned bounding box — a cheap reject before the full ray-cast.
const BBOX = HOLON_BOUNDARY.reduce(
  (b, [lng, lat]) => ({
    minLng: Math.min(b.minLng, lng), maxLng: Math.max(b.maxLng, lng),
    minLat: Math.min(b.minLat, lat), maxLat: Math.max(b.maxLat, lat),
  }),
  { minLng: Infinity, maxLng: -Infinity, minLat: Infinity, maxLat: -Infinity },
)

// Ray-casting point-in-polygon. (lat, lng) order in, [lng, lat] ring.
function pointInRing(lat, lng, ring) {
  let inside = false
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const xi = ring[i][0], yi = ring[i][1]
    const xj = ring[j][0], yj = ring[j][1]
    const intersect =
      yi > lat !== yj > lat &&
      lng < ((xj - xi) * (lat - yi)) / (yj - yi) + xi
    if (intersect) inside = !inside
  }
  return inside
}

/**
 * True when the coordinate falls inside Holon's municipal boundary.
 * Returns false for null/NaN input (no location = not bookable).
 */
export function isInServiceArea(lat, lng) {
  if (lat == null || lng == null || Number.isNaN(lat) || Number.isNaN(lng)) return false
  if (lng < BBOX.minLng || lng > BBOX.maxLng || lat < BBOX.minLat || lat > BBOX.maxLat) return false
  return pointInRing(lat, lng, HOLON_BOUNDARY)
}

