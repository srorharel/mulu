// Email-safe: table layout + inline styles. dir="rtl" on every element because
// Gmail strips the outer wrapper's dir. CTA button is #15803d (white text =
// 5:1, WCAG AA); brand #26b55f is the header band (large-text branding only).
import fs from 'node:fs'

const REF = 'fpwshpvixtgaygkuxajy'
