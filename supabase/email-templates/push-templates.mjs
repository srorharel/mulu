const URL = '{{ .ConfirmationURL }}'

// Token-hash landing on the marketing site (muluwash.com/auth/confirm). Used for
// the server-side-state flows (signup confirm / password recovery / email change)
// so the link no longer depends on window.location.origin — which on the native
// app is localhost and falls back to site_url (the marketing homepage). The page
// calls supabase.auth.verifyOtp({ token_hash, type }). magic_link + invite are NOT
// routed here (they must establish a session IN the app, not on this origin), and
// reauthentication uses a {{ .Token }} code, so all three keep {{ .ConfirmationURL }}.
const confirmUrl = (type) => `https://muluwash.com/auth/confirm?token_hash={{ .TokenHash }}&type=${type}`

function shell({ heading, body, cta, code, foot }) {
  const ctaBlock = cta ? `
        <table role="presentation" cellpadding="0" cellspacing="0" style="margin:0 0 22px;">
          <tr><td style="border-radius:12px;background:#15803d;">
            <a href="${cta.url}" target="_blank" style="display:inline-block;padding:15px 38px;font-size:16px;font-weight:bold;color:#ffffff;text-decoration:none;border-radius:12px;">${cta.label}</a>
          </td></tr>
        </table>` : ''
  const codeBlock = code ? `
        <table role="presentation" cellpadding="0" cellspacing="0" style="margin:0 0 22px;width:100%;">
          <tr><td align="center" style="background:#f0f7f2;border:1px solid #cfe6d8;border-radius:12px;padding:18px;">
            <span style="font-family:'Courier New',monospace;font-size:30px;font-weight:bold;letter-spacing:6px;color:#15803d;">${code}</span>
          </td></tr>
        </table>` : ''
  return `<!-- Generated by supabase/email-templates/push-templates.mjs — edit copy there, not here. -->
<div dir="rtl" style="margin:0;padding:24px;background:#f4f7f5;font-family:Arial,Helvetica,sans-serif;color:#1a1a1a;">
  <table dir="rtl" role="presentation" width="100%" cellpadding="0" cellspacing="0" style="max-width:560px;margin:0 auto;background:#ffffff;border-radius:16px;overflow:hidden;border:1px solid #e3e8e5;">
    <tr><td dir="rtl" style="background:#26b55f;padding:24px;text-align:right;">
      <p style="margin:0;font-size:22px;font-weight:bold;color:#ffffff;letter-spacing:0.5px;">MULU</p>
      <p style="margin:6px 0 0;font-size:13px;color:#eafaf0;">שטיפת רכב עד אליך</p>
    </td></tr>
    <tr><td dir="rtl" style="padding:28px 24px;text-align:right;">
      <p style="margin:0 0 8px;font-size:19px;font-weight:bold;color:#1a1a1a;">${heading}</p>
      <p style="margin:0 0 22px;font-size:14.5px;line-height:1.7;color:#555;">${body}</p>
${ctaBlock}${codeBlock}
      <p style="margin:0;font-size:12.5px;color:#aaa;line-height:1.6;">${foot}</p>
    </td></tr>
    <tr><td dir="rtl" style="padding:16px 24px;background:#f8faf9;border-top:1px solid #e3e8e5;font-size:11.5px;color:#999;text-align:right;">תודה שבחרת ב-MULU 💚</td></tr>
  </table>
</div>`
}

