for (const t of Object.values(templates)) fs.writeFileSync(`${DIR}/${t.file}`, t.html, 'utf8')

if (process.env.PUSH === '1') {
  const body = {}
  for (const t of Object.values(templates)) { body[t.sk] = t.subject; body[t.ck] = t.html }
  const res = await fetch(`https://api.supabase.com/v1/projects/${REF}/config/auth`, {
    method: 'PATCH',
    headers: { Authorization: `Bearer ${process.env.SUPA_TOKEN}`, 'Content-Type': 'application/json' },
    body: JSON.stringify(body),
  })
  const j = await res.json().catch(() => ({}))
  console.log('HTTP', res.status)
  for (const t of Object.values(templates)) console.log(`${t.sk}:`, JSON.stringify(j[t.sk]), '| chars:', (j[t.ck] || '').length)
} else {
  console.log('Wrote', Object.keys(templates).length, 'template files. Set PUSH=1 + SUPA_TOKEN to push.')
}
